#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	neg;

	i = 0;
	num = 0;
	neg = 1;
	while (*(str + i) != '\0' && ((*(str + i) > 8 && *(str + i) < 14)
			|| (*(str + i) == 32)))
		i++;
	if (*(str + i) == '-' || *(str + i) == '+')
	{
		if (*(str + i) == '-')
			neg = -1;
		i++;
	}
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		num = (num * 10) + *(str + i) - '0';
		i++;
	}
	return (num * neg);
}
