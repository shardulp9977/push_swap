#include "libft.h"

int	ft_valid(char **s)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (s[i] != NULL)
	{
		j = 0;
		while (s[i][j] != '\0')
		{
			if (!((s[i][j] >= '0' && s[i][j] <= '9')
				|| s[i][j] == 32 || s[i][j] == '-'))
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
